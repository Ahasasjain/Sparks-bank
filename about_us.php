   The second module is to send money from one customer of the bank to another , This is done by fetching the data of customers from 
   Customer_list. When the user press send money button , a prompt is generated on screen which ask for details required to perform transaction.<br/><br/>
   The third module is to view the list of all the transactions that has been performed by different users , This is done by fetching
   the data from the table named transactions in the database named sparks_bank. 

